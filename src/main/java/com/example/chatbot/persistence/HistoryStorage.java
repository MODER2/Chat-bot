package com.example.chatbot.persistence;

import com.example.chatbot.model.Message;

import java.util.List;

/**
 * HistoryStorage — ИНТЕРФЕЙС хранилища истории сообщений.
 * <p>
 * Описывает «контракт» сохранения и загрузки переписки, не уточняя, КУДА именно
 * сохраняем. Сегодня это файл ({@link FileHistoryStorage}), а завтра, не меняя
 * остального кода, можно сделать реализацию для базы данных или облака.
 * Это тот же приём «программирование на уровне интерфейсов», что и у {@code IBot}.
 */
public interface HistoryStorage {

    /**
     * Сохраняет все сообщения (обычно вызывается при выходе из программы).
     *
     * @param messages список сообщений для сохранения
     */
    void save(List<Message> messages);

    /**
     * Загружает ранее сохранённые сообщения (обычно вызывается при запуске).
     *
     * @return список сообщений; пустой список, если истории ещё нет
     */
    List<Message> load();
}
