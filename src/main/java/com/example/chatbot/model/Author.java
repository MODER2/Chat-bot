package com.example.chatbot.model;

/**
 * Author — ПЕРЕЧИСЛЕНИЕ (enum) авторов сообщения.
 * <p>
 * Перечисление используется тог, когда значений строго ограниченное количество.
 * В нашем чате сообщение может прислать только два «лица»:
 * <ul>
 *     <li>{@link #USER} — пользователь (человек, который печатает в окне);</li>
 *     <li>{@link #BOT}  — бот (программа, которая отвечает).</li>
 * </ul>
 * <p>
 * Почему enum, а не строка {@code "user"}/{@code "bot"}? Потому что компилятор
 * не даст ошибиться: нельзя написать {@code Author.USR} — будет ошибка на этапе
 * компиляции, а строку "usr" легко опечатать и поймать баг уже во время работы.
 */
public enum Author {

    /** Сообщение написал пользователь. */
    USER("Вы"),

    /** Сообщение написал бот. */
    BOT("Бот");

    /**
     * «Человекочитаемое» имя автора, которое показываем в интерфейсе.
     * final — значит, что после создания изменить его нельзя (константа).
     */
    private final String displayName;

    /**
     * Конструктор перечисления. Вызывается автоматически для каждой константы
     * (USER и BOT) при загрузке класса.
     *
     * @param displayName подпись, которую увидит пользователь
     */
    Author(String displayName) {
        this.displayName = displayName;
    }

    /**
     * @return подпись автора для отображения в окне чата (например, "Вы" или "Бот")
     */
    public String getDisplayName() {
        return displayName;
    }
}
