package com.example.chatbot.bot;

import com.example.chatbot.bot.handler.MessageHandler;

import java.util.List;

/**
 * IBot — ИНТЕРФЕЙС бота (программный «контракт»).
 * <p>
 * Интерфейс описывает, ЧТО умеет любой бот, но не говорит, КАК это сделано.
 * Это и есть «программный интерфейс» из требований задания. Благодаря ему
 * остальная программа (например, окно чата) зависит не от конкретной реализации,
 * а от абстракции: окну всё равно, отвечает ли бот по простым правилам или,
 * скажем, через нейросеть — лишь бы он реализовывал {@code IBot}.
 * <p>
 * Такой подход называется «программирование на уровне интерфейсов» и делает код
 * слабосвязанным: одну реализацию бота легко заменить другой.
 * <p>
 * Именно потому, что {@code IBot} ничего не знает про JavaFX, тот же бот можно
 * подключить и к онлайн-версии (например, к Telegram-боту).
 */
public interface IBot {

    /**
     * Главный метод: получает сообщение пользователя и возвращает ответ бота.
     *
     * @param userInput текст, который ввёл пользователь
     * @return текст ответа бота (никогда не null)
     */
    String respond(String userInput);

    /**
     * Регистрирует новый обработчик сообщений. Это «точка расширения»: добавив
     * сюда новый {@link MessageHandler}, мы учим бота новой команде.
     *
     * @param handler обработчик, который нужно добавить
     */
    void registerHandler(MessageHandler handler);

    /**
     * @return список всех зарегистрированных обработчиков (для справки/отладки)
     */
    List<MessageHandler> getHandlers();

    /**
     * @return отображаемое имя бота
     */
    String getBotName();
}
