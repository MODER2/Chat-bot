package com.example.chatbot.bot.handler;

import com.example.chatbot.bot.BotContext;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 * MessageHandler — АБСТРАКТНЫЙ КЛАСС, описывающий «обработчик одного типа сообщений».
 * <p>
 * Это ключевой элемент гибкости бота. Идея такая: бот не содержит один огромный
 * метод с тысячей {@code if-else}. Вместо этого он хранит СПИСОК обработчиков.
 * Каждый обработчик отвечает за одну тему: приветствие, время, калькулятор и т.д.
 * Чтобы научить бота новой команде, достаточно написать новый класс-наследник
 * этого абстрактного класса и зарегистрировать его — старый код менять не нужно
 * (это принцип «открыт для расширения, закрыт для изменения»).
 * <p>
 * Почему именно АБСТРАКТНЫЙ КЛАСС, а не интерфейс? Потому что у всех обработчиков
 * есть общая часть реализации — работа с регулярным выражением (поле {@link #pattern}
 * и метод {@link #canHandle(String)}). Абстрактный класс позволяет один раз написать
 * эту общую логику, а наследникам оставить только «уникальную» часть — построение
 * ответа в методе {@link #respond(String, BotContext)}.
 * <p>
 * <b>Регулярные выражения (regex)</b> — главный инструмент распознавания команд.
 * Pattern компилируется один раз в конструкторе, далее используется многократно.
 * Флаги:
 * <ul>
 *     <li>{@link Pattern#CASE_INSENSITIVE} — игнорировать регистр («Привет» = «привет»);</li>
 *     <li>{@link Pattern#UNICODE_CASE} — чтобы регистронезависимость работала и для
 *         кириллицы, а не только для латиницы.</li>
 * </ul>
 */
public abstract class MessageHandler {

    /** Скомпилированное регулярное выражение для распознавания «своих» сообщений. */
    private final Pattern pattern;

    /**
     * Конструктор доступен только наследникам (protected). Принимает строку
     * регулярного выражения, компилирует её и сохраняет.
     *
     * @param regex регулярное выражение, по которому обработчик «узнаёт» команду
     */
    protected MessageHandler(String regex) {
        this.pattern = Pattern.compile(regex, Pattern.CASE_INSENSITIVE | Pattern.UNICODE_CASE);
    }

    /**
     * Проверяет, подходит ли данное сообщение этому обработчику.
     * <p>
     * Используется {@link Matcher#find()} (а не {@code matches()}), потому что
     * нам достаточно найти совпадение ГДЕ-ТО внутри строки, а не требовать,
     * чтобы вся строка целиком совпала с шаблоном.
     *
     * @param input текст сообщения пользователя
     * @return true, если обработчик берётся отвечать на это сообщение
     */
    public boolean canHandle(String input) {
        if (input == null) {
            return false;
        }
        return pattern.matcher(input.trim()).find();
    }

    /**
     * Вспомогательный метод для наследников: возвращает новый {@link Matcher}
     * по тому же шаблону. Нужен, чтобы достать «группы захвата» — например,
     * числа из команды «умножь 12 на 157».
     *
     * @param input текст сообщения
     * @return Matcher, на котором уже можно вызвать find() и group(n)
     */
    protected Matcher matcher(String input) {
        return pattern.matcher(input.trim());
    }

    /**
     * Короткое имя обработчика (для отладки и для списка возможностей бота).
     *
     * @return название, например «Калькулятор»
     */
    public abstract String getName();

    /**
     * Описание того, что умеет обработчик, и пример команды.
     * Используется командой «помощь», чтобы рассказать пользователю о возможностях.
     *
     * @return человекочитаемое описание, например «умножь 12 на 157»
     */
    public abstract String getDescription();

    /**
     * Главный метод: строит ОТВЕТ бота на сообщение.
     * Вызывается только если {@link #canHandle(String)} вернул true.
     *
     * @param input   текст сообщения пользователя
     * @param context контекст бота (история, имя пользователя и т.п.)
     * @return текст ответа бота
     */
    public abstract String respond(String input, BotContext context);
}
